package com.gokkiri.state;

public class StateModel {
	private int state_no;
	private String state_org;
	private String state_sav;
	private String state_name;
	private String state_addr;
	public int getState_no() {
		return state_no;
	}
	public void setState_no(int state_no) {
		this.state_no = state_no;
	}
	public String getState_org() {
		return state_org;
	}
	public void setState_org(String state_org) {
		this.state_org = state_org;
	}
	public String getState_sav() {
		return state_sav;
	}
	public void setState_sav(String state_sav) {
		this.state_sav = state_sav;
	}
	public String getState_name() {
		return state_name;
	}
	public void setState_name(String state_name) {
		this.state_name = state_name;
	}
	public String getState_addr() {
		return state_addr;
	}
	public void setState_addr(String state_addr) {
		this.state_addr = state_addr;
	}
	
	
	
	
	
}
